/** Get list of all district names (জেলা) in Bengali */
export function getDistricts(): string[] {
  return BANGLADESH_DISTRICTS_WITH_CITIES.map((d) => d.district);
}

/** Get cities/upazilas (শহর/উপজেলা) for a given district */
export function getCitiesForDistrict(district: string): string[] {
  const found = BANGLADESH_DISTRICTS_WITH_CITIES.find(
    (d) => d.district === district
  );
  return found ? found.cities : [];
}
